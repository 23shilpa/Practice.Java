package com.bridgelabz;

public class IfElseIfExample {
    public static void main(String[] args) {
        int marks = 50;
        if(marks<50){
            System.out.println("fail");
        }
        else if(marks>40){
            System.out.println("pass");
        }
        else{
            System.out.println("Invalid");
        }
    }
}
